    ফ্লাইট
    বিতর্ক

    ভুটান যাওয়ার জন্য ৬ তারিখ সকাল সাড়ে নয়টায় ফ্লাইট হওয়ার কথা ছিল। আগের দিন বিকেলে ফোন করে জানানো হয় টেকনিক্যাল সমস্যার কারণে ফ্লাইট ক্যানসেল। ডিসেম্বরের ১৮ তারিখে টিকেট কিনে রেখেছিলাম। টিকেট কেনার পর সময় গুলো কেন জানি যেতে চায় না। অপেক্ষা। এখন আবার ফ্লাইট ক্যানসেল। জানালো ৭ তারিখে রিসিডিউল করা হয়েছে।

    চার দিন ঘুরার সময় নিয়ে টিকেট কেটেছিলাম। এখন সব এলো মেলো হয়ে যাবে। ৭ তারিখে যদি ফ্লাইট হয়, তাহলে ১০ তারিখে ফিরলে ঠিক মত ঘুরা হবে না। তিন দিন সময় থাকবে হাতে। আবার ১০ তারিখের পরের রিটার্ন ফ্লাইট হচ্ছে ১২ তারিখে।

    ৭ তারিখ সকাল সকাল রেডি হয়ে বের হয়ে পড়লাম। বাসা থেকে এয়ারপোর্ট আসতে মাত্র ১০ মিনিট লেগেছে! সম্ভবত সাড়ে ছয়টায় পৌঁছে গিয়েছি, ফ্লাইট সাড়ে নয়টায়। এত আগে আগে না আসলেও হতো। যাই হোক, কফি স্টল থেকে কফি নিয়ে অপেক্ষা করতে লাগলাম।

    চেকইন শুরু হলে বোর্ডিং পাস নিলাম। ইমিগ্রেশন ফরম পূরণ করে ইমিগ্রেশন পার হলাম। কয়েক দিন আগে ইন্দোনেশিয়াতে যাওয়ার সময় যে ইমিগ্রেশন অফিসার ছিল, আজও উনি। ইমিগ্রেশন শেষে অপেক্ষা, ফ্লাইটের জন্য। প্রায় দেড় ঘন্টার মত সময় হাতে আছে। সব ঝামেলা শেষ। তাই ভালো।

    প্রথম দিন

    ভুটানে একটা মাত্র ইন্টারন্যাশনাল এয়ারপোর্ট, এবং তা পারতে। এদের রাজধানী হচ্ছে থিম্পুতে। পারো এয়ারপোর্ট ইন্টারন্যাশনাল হলেও ছোট। আর ছোট হলেও পরিপাটি। পুরা এলাক পাহাড়ি হওয়াতে কোন সমতল ভূমি নেই। ভুটানে ঢুকার পর একটা নদীর উপর দিয়ে বিমান ঢুকছিল। মনে হচ্ছিল যেন যে কোন সময়ই পাহাড়ের সাথে লেগে যাবে। নদীর উপর দিকে এঁকে বেঁকে এক সময় পারো এয়ারপোর্টে এসে নামল।

    বিমান থেকে নামার পরই ঠান্ডার স্পর্শ পাওয়া যাবে। রোদ্দ্রউজ্জল দিন হওয়ার পরও কেমন ঠান্ডা। গায়ে হালকা শীতের জামা ছিল। এয়ারপোর্টের টার্মিনালে ঢুকে প্রথমেই জ্যাকেট পরে নিলাম। এরপর ইমিগ্রেশন ফরম পূরণ করে এগিয়ে গেলাম ইমিগ্রেশন করার জন্য। ইমিগ্রেশন অফিচার মর্নিং জানালো। আমি পাসপোর্ট আর ইমিগ্রেশন ফরম দেওয়ার পর কয়দিন থাকব জিজ্ঞেস করে ভিসা এবং অ্যারাইভাল সিল মেরে দিল। ইমিগ্রেশন শেষ!

    আমার প্ল্যান ছিল পারোতে পরে ঘুরব। আগে থিম্পু এবং পুনাখা ঘুরে যাবো। পুনাখা যেতে হলে পারমিশন লাগে, আর তা পেতে হলে থিম্পু ইমিগ্রেশন অফিস থেকে নিতে হয়। আমি পৌঁছেছি রবিবারে। রবিবারে তাদের ছুটির দিন। সুতারাং আমাকে থিম্পুতে একদিন থাকতে হবে। এয়ারপোর্ট থেকে বের হয়ে খুঁজতে ছিলাম শেয়ার্ড ভাবে থিম্পু আসা যায় কিনা। ঐখানে ট্যাক্সি ছিল, কিন্তু একা ট্যাক্সি নিয়ে গেলে ১৫০০ রুপির মত দিতে হবে। ট্যাক্সি ড্রাইভারকে জানালাম আমি শেয়ার্ড ভাবে যেতে চাচ্ছি। ওরা বলল এখান থেকে শেয়ার্ড ভাবে যাওয়া যাবে না। আমি ভাবলাম উনারা শেয়ার্ড ভাবে না গেলে আমি দেখি ম্যানেজ করতে পারি কিনা। একই ফ্লাইটে আসা দুইজন থিম্পুতে যাবে। তাদের সাথে যাওয়া যাবে কিনা জিজ্ঞেস করলাম, তারা রাজি হলো। বেঁকে বসল ট্যাক্সি ড্রাইভাররা। এখান থেকে তারা শেয়ার্ড ভাবে যেতে দিবে না।

    এয়ারপোর্টে এক ভদ্রলোকের সাথে কথা হয়েছে। মাননুন কাদের, সাউথটেক লিমিটেডের MD।  ভুটানে সফটওয়ার বিজনেস আছে উনাদের। আমাকে কিছু টিপস দিল। যেমন বলল অনেক উঁচু জায়গা হওয়ার কারণে বাতাসে অক্সিজেন কম। আর তাই মাথা ব্যাথা করবে। আর তখন বেশি বেশি করে শ্বাস নিতে হবে। এছাড়াও বিমানে বসেও কিছুক্ষণ কথা হলো। বলল বিমান থেকে এভারেস্ট দেখা যাবে। আমি জিজ্ঞেস করলাম কোন পাশ থেকে দেখা যাবে। আমাকে দেখিয়ে দিল। ঢাকা থেকে ভুটান যেতে হাতের বাম পাশ থেকে দেখা যাবে। আর ফেরার পথে ডান পাশ থেকে। আমি গিয়ে আমার সিটে বসলাম। আমার সিটও বাম পাশে ছিল, উইন্ডো সিট। যদিও বিমানে যাত্রী অনেক কম ছিল, যে কোন জায়গায় চাইলেই বসা যেতো।
